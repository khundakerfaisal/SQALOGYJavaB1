package SecondClass;

public class loopProgram {
    public static void main(String[] args) {
        //for loop
        for(int i=0;i<=5;i++){ //for loop
            System.out.println("For loop value is : " +i);
        }
        //while loop
        int i=0;
        while (i<=5){
            System.out.println("While loop value is : "+i);
            i++;
        }
        //do-while loop
        int j=0;
        do{
            System.out.println("do-while value is : " +j);
            i++;
        }while (i<=5);

    }
}
